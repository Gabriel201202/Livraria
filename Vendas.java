public class Vendas {
  double Frete;
  int Estoque;
  double Compra;

  public void setFrete(Double Frete) {
    this.Frete = Frete;
  }
  public double getFrete() {
    return Frete;
  }
  public void setEstoque(int Estoque) {
    this.Estoque = Estoque;
  }
  public int getEstoque() {
    return Estoque;
  }
  public void setCompra(Double Compra) {
    this.Compra = Compra;
  }
  public double getCompra() {
    return Compra;
  }
}
